package mgh.链表.链表基本操作;
/**
* @author maguohao
* @version 创建时间：2018年9月6日 下午11:43:20
* 类说明:
* 节点类
* 1、数据域（可能是一个简单的变量，也可能是复杂的类）
* 2、链域:下一个节点的地址
*/
public class Node {

	public String name;//节点数据域
	
	public Node next;//下一个节点的地址
	
	public Node() {
		this.name = name;
		this.next = next;
	}
	
	public Node(String name) {
		this.name = name;
		this.next = next;
	}

	
	
}
